void DrawRandomRect()
{
	for (int i{}; i < 100; ++i)
	{
		Sleep(1);
		Point2f position{ GenerateRandomFloat(10,g_WindowWidth-10),GenerateRandomFloat(10,g_WindowHeight-10) };
		float width{ rand() % 500 + 1.f };
		float height{ rand() % 500 + 1.f };
		float r{ GenerateRandomFloat(0,256)/256 }, g{ GenerateRandomFloat(0,256)/256 }, b{ GenerateRandomFloat(0,256)/256 }, a{ 1.f };
		SetColor(r, g, b, a);
		FillRect(position.x, position.y, width, height);
		SetColor(0, 0, 0);
		DrawRect(position.x, position.y, width, height);
	}
}
float GenerateRandomFloat(float min, float max)
{
	return float(((rand() % (int)max*100.f) + min*100.f)/100.f);
}
